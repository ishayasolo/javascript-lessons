// 3 ways of declaring variables in JavaScript
// - var
var age = 100;
console.log(age);

// - let
// declaration
let name;

// initialization
name = "Ishaya";
console.log("former name: " + name)

// initialization
name = "Favour"
console.log("current name: " + name)

// - const
const gender = "male";
console.log("gender: " + gender)


// operators
//  + * / - %
console.log("the value of 5 + 5 = " + (5 + 5));
console.log("the value of 3 x 4 = " + (3 * 4));

// operands - the values that make up an operation
console.log("the value of the operation (4 x 5) + 10 = " + ((4 * 5) + 10)); 

// operations - a combination of operands and operators

